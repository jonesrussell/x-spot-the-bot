import { promises as fs } from 'fs';
import { join } from 'path';

async function generateIcons() {
  try {
    // Create a minimal 1x1 pixel PNG file
    const minimalPNG = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
      0x00, 0x00, 0x00, 0x0D,
      0x49, 0x48, 0x44, 0x52,
      0x00, 0x00, 0x00, 0x01,
      0x00, 0x00, 0x00, 0x01,
      0x08, 0x06, 0x00, 0x00, 0x00,
      0x1F, 0x15, 0xC4, 0x89,
      0x00, 0x00, 0x00, 0x0A,
      0x49, 0x44, 0x41, 0x54,
      0x08, 0xD7, 0x63, 0x00, 0x00,
      0x00, 0x02, 0x00, 0x01,
      0xE5, 0x27, 0xDE, 0x97,
      0x00, 0x00, 0x00, 0x00,
      0x49, 0x45, 0x4E, 0x44,
      0xAE, 0x42, 0x60, 0x82
    ]);

    // Write directly to dist/icons
    const distIconDir = join('dist', 'icons');
    await fs.mkdir(distIconDir, { recursive: true });
    
    const sizes = [16, 32, 48, 128];
    for (const size of sizes) {
      await fs.writeFile(join(distIconDir, `icon${size}.png`), minimalPNG);
    }

    console.log('âœ“ Icons generated successfully');
  } catch (error) {
    console.error('Error creating minimal icons:', error);
  }
}

generateIcons(); 